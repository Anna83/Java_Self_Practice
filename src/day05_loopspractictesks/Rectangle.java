package day05_loopspractictesks;

public class Rectangle {
    public static void main(String[] args) {
        int givenNumber = 9;

        for (int a = 1; a <= givenNumber; a++) {
            for (int b = 1; b < givenNumber; b++  ){
                System.out.print( "*" + " ");
            }
            System.out.println();

        }


    }
}
