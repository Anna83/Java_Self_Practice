package day05_loopspractictesks;

public class EnglishAlphabet {
    public static void main(String[] args) {
//        2. Create a class named EnglishAlphabets, and write a program
//        that displays alphabet letters from A to Z in the
//                same line separated by a space.
//

        for (char i = 'A'; i <='Z'; i++) {
            System.out.print(i + " ");



        }
        System.out.println();
    }
}